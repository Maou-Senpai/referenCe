#include <stdio.h>

int main() {
	int sampleInt = 1;
	short sampleShort = 2;
	long sampleLong = 3;
	char sampleChar = 'a';
	float sampleFloat = 4.1;
	double sampleDouble = 5.2;
	
	printf("%d\n", sampleInt);
	printf("%d\n", sampleShort);
	printf("%ld\n", sampleLong);
	printf("%c\n", sampleChar);
	printf("%f\n", sampleFloat);
	printf("%lf", sampleDouble);
}
